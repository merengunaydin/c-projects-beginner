#include <stdio.h>

int main()
{
    char letter = 'C';
    int number1 = 100;
    float number2 = 3500;
    double number3 = 146000;

    printf("I have two sides: %c \n", letter);
    printf("My lucky number is: %d \n", number1);
    printf("Hi! %f \n", number2);
    printf("It is a giant number: %lf \n", number3);

    return 0;
}
